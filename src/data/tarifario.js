// Función para normalizar nombres de ciudades
export const normalizeCityName = (city) => {
  return city.toLowerCase()
    .trim()
    .replace(/[áàäâ]/g, 'a')
    .replace(/[éèëê]/g, 'e')
    .replace(/[íìïî]/g, 'i')
    .replace(/[óòöô]/g, 'o')
    .replace(/[úùüû]/g, 'u')
    .replace(/ñ/g, 'n')
    .replace(/[^a-z0-9\s]/g, '');
};

// Función para normalizar nombres de estados
export const normalizeStateName = (state) => {
  if (!state || state.trim() === '') {
    return '';
  }
  
  const upperState = state.toUpperCase().trim();
  
  // Buscar por nombre completo
  for (const [abbrev, fullName] of Object.entries(MEXICAN_STATES)) {
    if (fullName.toLowerCase() === state.toLowerCase()) {
      return abbrev;
    }
  }
  
  // Si ya es una abreviación válida, devolverla
  if (MEXICAN_STATES[upperState]) {
    return upperState;
  }
  
  return upperState;
};

// Función para buscar entrada en el tarifario
export const findTarifarioEntry = (city, state) => {
  const normalizedCity = normalizeCityName(city);
  const normalizedState = normalizeStateName(state);
  
  console.log('🔍 Buscando en tarifario JS:', { city, state, normalizedCity, normalizedState });
  
  // Buscar coincidencia exacta primero
  let entry = tarifarioData.find(entry => {
    const entryCity = entry.normalizedDestino;
    const entryState = entry.estado.toUpperCase();
    
    const cityMatch = entryCity === normalizedCity || 
                     entryCity.includes(normalizedCity) || 
                     normalizedCity.includes(entryCity);
    const stateMatch = entryState === normalizedState;
    
    return cityMatch && stateMatch;
  });
  
  if (!entry) {
    // Buscar servicio local para el estado o Reynosa para Tamaulipas
    console.log('🔄 Buscando servicio local para estado:', normalizedState);
    
    if (normalizedState === 'TAMPS') {
      // Para Tamaulipas, usar Reynosa como servicio local
      entry = tarifarioData.find(entry => {
        const entryCity = entry.normalizedDestino;
        const entryState = entry.estado.toUpperCase();
        return entryCity === 'reynosa' && entryState === 'TAMPS';
      });
      console.log('🏠 Reynosa (servicio local Tamaulipas) encontrado:', entry ? { destino: entry.destino, estado: entry.estado } : 'null');
    } else {
      // Para otros estados, buscar servicio local
      entry = tarifarioData.find(entry => {
        const entryCity = entry.normalizedDestino;
        const entryState = entry.estado.toUpperCase();
        return entryCity.includes('servicio local') && entryState === normalizedState;
      });
      console.log('🏠 Servicio local encontrado:', entry ? { destino: entry.destino, estado: entry.estado } : 'null');
    }
  }
  
  console.log('✅ Entrada encontrada:', entry ? { destino: entry.destino, estado: entry.estado } : 'null');
  return entry;
};

// Función para calcular precio
export const calculatePrice = (
  pickupCity,
  pickupState,
  deliveryCity,
  deliveryState,
  vehicleType,
  priority = 'estandar'
) => {
  console.log('💰 calculatePrice llamado con:', {
    pickupCity,
    pickupState,
    deliveryCity,
    deliveryState,
    vehicleType,
    priority
  });

  // Normalizar ciudades y estados
  const normalizedPickupCity = normalizeCityName(pickupCity);
  const normalizedPickupState = normalizeStateName(pickupState);
  const normalizedDeliveryCity = normalizeCityName(deliveryCity);
  const normalizedDeliveryState = normalizeStateName(deliveryState);

  console.log('🔄 Ciudades normalizadas:', {
    pickup: `${normalizedPickupCity}, ${normalizedPickupState}`,
    delivery: `${normalizedDeliveryCity}, ${normalizedDeliveryState}`
  });

  // LÓGICA CORRECTA DEL TARIFARIO:
  // 1. LOCAL: CDMX → CDMX (servicio local)
  // 2. TARIFA AUTOMÁTICA: Recolección en CDMX → Entrega a otra ciudad
  // 3. COTIZACIÓN MANUAL: Recolección en otra ciudad → CDMX (o cualquier otra combinación no cubierta)

  // Verificar si el pickup es CDMX
  const isCDMX = (city, state) => {
    const normalizedCity = normalizeCityName(city);
    const normalizedState = normalizeStateName(state);
    return (normalizedState === 'CDMX' || 
            normalizedCity.includes('mexico') || 
            normalizedCity.includes('cdmx') ||
            normalizedCity.includes('ciudad de mexico'));
  };

  const isPickupCDMX = isCDMX(pickupCity, pickupState);
  const isDeliveryCDMX = isCDMX(deliveryCity, deliveryState);

  console.log('🏙️ Verificación CDMX:', {
    isPickupCDMX,
    isDeliveryCDMX
  });

  // Caso 1: Si la recolección NO es CDMX → Cotización Manual
  if (!isPickupCDMX) {
    console.log('❌ Cotización manual requerida: La recolección debe ser desde CDMX');
    return {
      found: false,
      error: 'Cotización manual requerida. El tarifario automático solo aplica para envíos que salen de CDMX.',
      distance: null,
      basePrice: null,
      finalPrice: null
    };
  }

  // Caso 2: CDMX → CDMX (Servicio Local)
  if (isPickupCDMX && isDeliveryCDMX) {
    console.log('🏠 Buscando servicio local CDMX → CDMX');
    const entry = findTarifarioEntry('servicio local', 'CDMX');
    
    if (!entry) {
      console.log('❌ No se encontró servicio local para CDMX');
      return {
        found: false,
        error: 'Servicio local CDMX no encontrado en el tarifario',
        distance: null,
        basePrice: null,
        finalPrice: null
      };
    }

    const vehicleColumn = VEHICLE_TYPE_MAPPING[vehicleType];
    if (!vehicleColumn) {
      return {
        found: false,
        error: `Tipo de vehículo no válido: ${vehicleType}`,
        distance: null,
        basePrice: null,
        finalPrice: null
      };
    }

    const basePrice = entry[vehicleColumn];
    if (!basePrice || basePrice === 0) {
      return {
        found: false,
        error: `Precio no disponible para ${vehicleType}`,
        distance: null,
        basePrice: null,
        finalPrice: null
      };
    }

    // Aplicar multiplicadores según prioridad
    let multiplier = 1;
    switch (priority) {
      case 'economico':
        multiplier = 0.85; // -15%
        break;
      case 'urgente':
        multiplier = 1.15; // +15%
        break;
      case 'estandar':
      default:
        multiplier = 1; // +0%
        break;
    }

    const finalPrice = Math.round(basePrice * multiplier * 100) / 100;

    console.log('✅ Servicio local CDMX calculado:', {
      destination: entry.destino,
      vehicleType,
      basePrice,
      priority,
      multiplier,
      finalPrice
    });

    return {
      found: true,
      distance: entry.km,
      basePrice,
      finalPrice,
      entry,
      vehicleType,
      priority
    };
  }

  // Caso 3: CDMX → Otra ciudad (Tarifario automático)
  console.log('🚛 Buscando tarifa CDMX → Otra ciudad');
  const entry = findTarifarioEntry(deliveryCity, deliveryState);
  
  if (!entry) {
    console.log('❌ Ciudad de destino no encontrada en el tarifario');
    return {
      found: false,
      error: 'Ruta no encontrada en el tarifario. Se requiere cotización manual.',
      distance: null,
      basePrice: null,
      finalPrice: null
    };
  }
  
  const vehicleColumn = VEHICLE_TYPE_MAPPING[vehicleType];
  if (!vehicleColumn) {
    return {
      found: false,
      error: `Tipo de vehículo no válido: ${vehicleType}`,
      distance: null,
      basePrice: null,
      finalPrice: null
    };
  }
  
  const basePrice = entry[vehicleColumn];
  if (!basePrice || basePrice === 0) {
    return {
      found: false,
      error: `Precio no disponible para ${vehicleType}`,
      distance: null,
      basePrice: null,
      finalPrice: null
    };
  }
  
  // Aplicar multiplicadores según prioridad
  let multiplier = 1;
  switch (priority) {
    case 'economico':
      multiplier = 0.85; // -15%
      break;
    case 'urgente':
      multiplier = 1.15; // +15%
      break;
    case 'estandar':
    default:
      multiplier = 1; // +0%
      break;
  }
  
  const finalPrice = Math.round(basePrice * multiplier * 100) / 100;
  
  console.log('✅ Tarifa CDMX → Otra ciudad calculada:', {
    destination: entry.destino,
    vehicleType,
    basePrice,
    priority,
    multiplier,
    finalPrice
  });
  
  return {
    found: true,
    distance: entry.km,
    basePrice,
    finalPrice,
    entry,
    vehicleType,
    priority
  };
};

